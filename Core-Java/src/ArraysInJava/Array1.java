package ArraysInJava;

public class Array1 {
	public static void main(String[] args) {
		int[] age = new int[3];
		age[0]=36;
		age[1]=27;
		age[2]=28;
		
		System.out.println(age[0]);  //36
		System.out.println(age[1]);  //27
		System.out.println(age[2]);  //28
	}
}
