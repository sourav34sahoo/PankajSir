package ArraysInJava;

public class Array2 {
	public static void main(String[] args) {
		String[] name = new String[3];
		name[0]="Sourav";
		name[1]="kumar";
		name[2]="sahoo";
		
		System.out.println(name[0]);  //sourav
		System.out.println(name[1]);  //kumar
		System.out.println(name[2]);  //sahoo
	}
}
